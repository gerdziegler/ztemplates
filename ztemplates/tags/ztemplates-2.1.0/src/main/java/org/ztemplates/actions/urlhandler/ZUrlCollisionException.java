package org.ztemplates.actions.urlhandler;

public class ZUrlCollisionException extends Exception
{

  public ZUrlCollisionException()
  {
    super();
  }


  public ZUrlCollisionException(String message, Throwable cause)
  {
    super(message, cause);
  }


  public ZUrlCollisionException(String message)
  {
    super(message);
  }


  public ZUrlCollisionException(Throwable cause)
  {
    super(cause);
  }

}
