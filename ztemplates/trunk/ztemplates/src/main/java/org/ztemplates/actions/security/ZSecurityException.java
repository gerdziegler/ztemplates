package org.ztemplates.actions.security;

public class ZSecurityException extends Exception
{

  public ZSecurityException()
  {
    super();
  }


  public ZSecurityException(String message, Throwable cause)
  {
    super(message, cause);
  }


  public ZSecurityException(String message)
  {
    super(message);
  }


  public ZSecurityException(Throwable cause)
  {
    super(cause);
  }

}
